
# inserting values
thislist = ["apple", "banana", "cherry"]
thislist.append("orange")   # adding element to the list
print(thislist)  


thislist = ["apple", "banana", "cherry"]
thislist.insert(0,"orange")  # adding element to the particular position using 
# index value
print(thislist)
       
 # removing values
thislist = [ "banana", "cherry","apple"]
thislist.remove("apple")  
print(thislist)  
 # removing values with help of index values
thislist = [ "banana", "cherry","apple"]
thislist.pop(1)  
print(thislist)